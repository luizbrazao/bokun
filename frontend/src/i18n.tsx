type I18nContextValue = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

function getByPath(dict: Dictionary, key: string): string | null {
  const parts = key.split(".");
  let current: string | Dictionary | undefined = dict;

  for (const part of parts) {
    if (typeof current !== "object" || current === null || !(part in current)) {
      return null;
    }
    current = (current as Dictionary)[part] as string | Dictionary;
  }

  return typeof current === "string" ? current : null;
}

function interpolate(template: string, vars?: Record<string, string | number>): string {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(vars[name] ?? `{${name}}`));
}

function detectInitialLocale(): Locale {
  const stored = window.localStorage.getItem(STORAGE_KEY);
  if (stored === "pt" || stored === "en" || stored === "es") return stored;

  const nav = navigator.language.toLowerCase();
  if (nav.startsWith("es")) return "es";
  if (nav.startsWith("en")) return "en";
  return "pt";
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocale] = useState<Locale>(() => detectInitialLocale());

  useEffect(() => {
    window.localStorage.setItem(STORAGE_KEY, locale);
    document.documentElement.lang = locale;
  }, [locale]);

  const value = useMemo<I18nContextValue>(() => {
    const t = (key: string, vars?: Record<string, string | number>) => {
      const localized = getByPath(dictionaries[locale], key);
      const fallback = getByPath(dictionaries.pt, key);
      return interpolate(localized ?? fallback ?? key, vars);
    };

    return { locale, setLocale, t };
  }, [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return ctx;
}

export function localeToBCP47(locale: Locale): string {
  if (locale === "en") return "en-US";
  if (locale === "es") return "es-ES";
  return "pt-BR";
}

export function formatDateByLocale(locale: Locale, value: number, options?: Intl.DateTimeFormatOptions): string {
  return new Date(value).toLocaleDateString(localeToBCP47(locale), options);
}

export function formatTimeByLocale(locale: Locale, value: number, options?: Intl.DateTimeFormatOptions): string {
  return new Date(value).toLocaleTimeString(localeToBCP47(locale), options);
}

export function formatDateTimeByLocale(locale: Locale, value: number, options?: Intl.DateTimeFormatOptions): string {
  return new Date(value).toLocaleString(localeToBCP47(locale), options);
}

export function formatTimeAgo(t: (key: string, vars?: Record<string, string | number>) => string, ts: number): string {
  const diff = Date.now() - ts;
  const min = Math.floor(diff / 60000);
  if (min < 1) return t("timeAgo.now");
  if (min < 60) return t("timeAgo.minutes", { value: min });
  const hours = Math.floor(min / 60);
  if (hours < 24) return t("timeAgo.hours", { value: hours });
  const days = Math.floor(hours / 24);
  return t("timeAgo.days", { value: days });
}
